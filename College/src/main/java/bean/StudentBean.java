package bean;

public class StudentBean {
	private String stdNO;
	private String stdName;
	private String stdHp;
	private String stdYear;
	private String stdAddres;
	public String getStdNO() {
		return stdNO;
	}
	public void setStdNO(String stdNO) {
		this.stdNO = stdNO;
	}
	public String getStdName() {
		return stdName;
	}
	public void setStdName(String stdName) {
		this.stdName = stdName;
	}
	public String getStdHp() {
		return stdHp;
	}
	public void setStdHp(String stdHp) {
		this.stdHp = stdHp;
	}
	public String getStdYear() {
		return stdYear;
	}
	public void setStdYear(String stdYear) {
		this.stdYear = stdYear;
	}
	public String getStdAddres() {
		return stdAddres;
	}
	public void setStdAddres(String stdAddres) {
		this.stdAddres = stdAddres;
	}
	
	

	
	
}
